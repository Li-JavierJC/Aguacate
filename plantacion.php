						Es recomendable dejar pasar por lo menos un mes después de abierta la ahoyadura para iniciar las labores de llenado. Los insumos necesarios para el llenado de agujeros son:
						<br>
						•	Tierra negra
						<br>
						•	Cal dolomítica
						<br>
						•	Insecticida-nematicida sistémico
						<br>
						•	Abono orgánico descompuesto
						<br><br>
						Los pasos para el llenado son los siguientes:
						<br>
						1.- Picar el fondo del agujero
						<br>
						2.- Aplicar media libra de cal dolomítica distribuida en el fondo y en las paredes.
						<br>
						3.- Tirar la primera capa de tierra superficial al fondo del agujero.
						<br>
						4.- Mezclar tierra negra más cinco paladas de abono orgánico y media libra de cal dolomítica para llenar hasta la mitad del agujero.
						<br>
						5.- Apisonar suavemente para eliminar bolsas de aire.
						<br>
						6.- Aplicar 15 g del insecticida granulado.
						<br>
						7.- Mezclar tierra negra más cinco paladas de abono orgánico y media libra de cal dolomítica para llenar hasta el nivel el agujero.
						<br>
						8.- Aplicar 15 g de insecticida granulado.
						<br>
						9.- Apisonar suavemente para eliminar bolsas de aire.
						<br>
						10.- Colocar nuevamente la estaca al centro del agujero.
						<br>
						11.- Concluir el llenado dejando un montículo de tierra por encima del nivel del suelo.
						<br>
						12.- Regar los agujeros cada semana con diez galones de agua durante un mes, para luego proceder al plantado de los árboles.


